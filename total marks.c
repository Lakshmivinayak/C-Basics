#include<stdio.h>
void main()
{
	// Variable Declaeration
	int phy, che, mat, eng, tot;
	
	// Initialization
	phy = 70;
	che = 78;
	mat = 87;
	eng = 90;
	
	// Calculation
	tot = phy+che+mat+eng;
	
	//Output 
	printf("Marks in Physics: %d\n", phy);
	printf("Marks in Chemistry: %d\n", che);
	printf("Marks in Maths: %d\n", mat);
	printf("Marks in English: %d\n", eng);
	printf("Total Marks: %d\n", tot);	
	
}
