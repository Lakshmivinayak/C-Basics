// Printing all arithmetic operations
#include<stdio.h>
void main()
{
	int a, b, s;
	a = 15;
	b = 5;
	 
	s = a+b;
	
	printf("sum of %d and %d is %d\n", a, b, s);
	
	// subtraction
	
    int r, f, d; 
	r = 15;
	f = 5;
	
	d = r-f;
	
	printf("difference between %d and %d is %d\n", r, f, d);
	
	// Multiplication
	
	int v, g, m;
	v = 15;
	g = 5;
	
	m = v*g;
	
	printf("multiplication of %d and %d is %d\n", v, g, m);
	
	// Division
	
	int c, w, D, R;
	c = 15;
	w = 5;
	  
	D = 15/5;
	
	printf("division of %d and %d is %d and remainder is %d\n", c, w, D, R);
	
	// Modulo Division
	
	int j, h, MD;
	j = 15;
	h = 5;
	
	MD = 15%5;
	
	printf("module division %d and %d is %d", j, h, MD);
}
